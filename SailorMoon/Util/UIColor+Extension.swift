import Foundation
import UIKit

extension UIColor {
    static let backgroundColor = {
        return UIColor(red: 238, green: 236, blue: 242, alpha: 1)
    }
    static let white = {
        return UIColor.init(red: 255, green: 255, blue: 255, alpha: 1)
    }
}
